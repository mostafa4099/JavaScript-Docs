//Normal Variable
var nameVar = "Mostafa"
console.log(nameVar);

//Declare Variable with let
let nameLet = "Mostafa"
console.log(nameLet);
nameLet = "Golam Mostafa"; //reassignable
console.log(nameLet);

//Declare Constant with const
const nameConst = "Mostafa"
console.log(nameConst);
nameConst = "Golam Mostafa"; //not reassignable
console.log(nameConst);